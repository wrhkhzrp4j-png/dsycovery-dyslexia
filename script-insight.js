// 🔊 ฟังก์ชันจัดการระบบเสียงอ่าน Text-to-Speech ประจำหน้า Insight
function toggleInsightSpeech() {
    if (!('speechSynthesis' in window)) {
        alert("ขออภัย เบราว์เซอร์นี้ไม่รองรับระบบการอ่านออกเสียงข้อความ");
        return;
    }

    // ถ้าหากระบบกำลังอ่านอยู่ -> กดซ้ำรอบนี้ให้สั่งหยุดเล่นเสียงทันที
    if (isSpeakingInsight) {
        window.speechSynthesis.cancel();
        isSpeakingInsight = false;
        return;
    }

    // ทำการกวาดเอาเนื้อหา Text ทั้งหมดข้างในกล่องสาระการวิเคราะห์เชิงลึกแบบแปรผันตามกลุ่มอายุมาแปลงเป็นเสียงพูด
    const ageGroupLabel = document.getElementById("age-badge").textContent;
    const rawContentText = document.getElementById("insight-content").innerText;
    const fullSpeechText = `หน้ารายงานเจาะลึกทักษะเฉพาะบุคคล สำหรับ ${ageGroupLabel} ข้อมูลสรุปเชิงลึกมีดังนี้ครับ ${rawContentText}`;

    const utterance = new SpeechSynthesisUtterance(fullSpeechText);
    utterance.lang = "th-TH"; // บังคับใช้น้ำเสียงสเน่ห์สำเนียงไทย
    utterance.rate = 1.0;     // ควบคุมสปีดจังหวะความเร็วปกติ

    utterance.onend = function() {
        isSpeakingInsight = false;
    };
    utterance.onerror = function() {
        isSpeakingInsight = false;
    };

    isSpeakingInsight = true;
    window.speechSynthesis.speak(utterance);
}